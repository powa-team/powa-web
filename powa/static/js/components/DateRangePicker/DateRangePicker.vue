<template>
  <div>
    <v-btn>
      <v-icon class="mr-2">
        {{ icons.mdiClockOutline }}
      </v-icon>
      <span>{{ rangeString }}</span>
      <v-menu v-model="menu" activator="parent" :close-on-content-click="false">
        <v-sheet class="d-flex" height="350" width="540" no-gutters>
          <v-sheet
            class="pa-6 flex-grow-1"
            style="border-right: 1px solid #dfdfdf"
          >
            <v-form ref="form" v-model="valid">
              <b>Absolute time range</b>
              <br />
              <br />
              From
              <br />
              <v-row>
                <v-col cols="auto">
                  <v-dialog v-model="fromDialog" activator="parent">
                    <template #activator="{ props }">
                      <v-btn
                        size="small"
                        :icon="icons.mdiCalendar"
                        v-bind="props"
                      >
                      </v-btn>
                    </template>
                    <v-date-picker
                      v-model="pickerFrom"
                      title="Select 'from' date"
                    >
                      <template #actions>
                        <v-btn
                          variant="text"
                          color="primary"
                          @click="cancelPicker"
                        >
                          Cancel
                        </v-btn>
                        <v-btn
                          variant="text"
                          class="bg-primary"
                          :disabled="pickerFrom == null"
                          @click="applyPickerFromDate"
                        >
                          OK
                        </v-btn>
                      </template>
                    </v-date-picker>
                  </v-dialog>
                </v-col>
                <v-col>
                  <v-text-field
                    v-model="inputFrom"
                    :rules="fromRules"
                    hide-details
                    hide-selected
                    density="compact"
                  ></v-text-field>
                </v-col>
              </v-row>
              <br />
              To
              <br />
              <v-row>
                <v-col cols="auto">
                  <v-dialog
                    v-model="toDialog"
                    activator="parent"
                    max-width="400"
                  >
                    <template #activator="{ props }">
                      <v-btn
                        size="small"
                        :icon="icons.mdiCalendar"
                        v-bind="props"
                      >
                      </v-btn>
                    </template>
                    <v-date-picker v-model="pickerTo" title="Select 'to' date">
                      <template #actions>
                        <v-btn
                          variant="text"
                          color="primary"
                          @click="cancelPicker"
                        >
                          Cancel
                        </v-btn>
                        <v-btn
                          variant="text"
                          class="bg-primary"
                          :disabled="pickerTo == null"
                          @click="applyPickerToDate"
                        >
                          OK
                        </v-btn>
                      </template>
                    </v-date-picker>
                  </v-dialog>
                </v-col>
                <v-col>
                  <v-text-field
                    v-model="inputTo"
                    :rules="toRules"
                    hide-details
                    hide-selected
                    density="compact"
                  ></v-text-field>
                </v-col>
              </v-row>
              <v-btn
                class="mt-4"
                :disabled="!valid"
                variant="elevated"
                @click="applyTimeRange"
                >Apply time range</v-btn
              >
            </v-form>
          </v-sheet>
          <v-sheet sm="5" width="180" class="overflow-auto">
            <v-list density="compact">
              <v-list-item
                v-for="(range, index) in quickOptions"
                :key="index"
                link
                @click.prevent="loadRangeShortcut(range)"
              >
                {{ range.display }}
              </v-list-item>
            </v-list>
          </v-sheet>
        </v-sheet>
      </v-menu>
    </v-btn>
    <v-btn @click="applyTimeRange">
      <v-icon>
        {{ icons.mdiReload }}
      </v-icon>
    </v-btn>
    <v-btn class="me-4" @click="zoomOut">
      <v-icon>
        {{ icons.mdiMagnifyMinusOutline }}
      </v-icon>
    </v-btn>
  </div>
</template>

<script setup>
import { computed, ref, watch } from "vue";
import { quickOptions } from "./options.ts";
import { dateMath, rangeUtil } from "@grafana/data";
import { icons } from "@/plugins/vuetify";
import store from "@/store";
import { toISO } from "@/utils/dates";

const menu = ref(false);

// The raw values (examples: 'now-24h', 'Tue Sep 01 2020 10:16:00 GMT+0200')
// Interaction with parent component is done with from/to props which
// are unix timestamps
const rawFrom = ref(store.rawFrom);
const rawTo = ref(store.rawTo);
// The values to display in the custom range from and to fields
// we don't use raw values because we may want to pick/change from and
// to in the form before applying changes
const inputFrom = ref(rawFrom.value);
const inputTo = ref(rawTo.value);

const pickerFrom = ref(null);
const pickerTo = ref(null);

const fromDialog = ref(false);
const toDialog = ref(false);

const form = ref(null);
const valid = ref(true);

const rangeString = computed(() => {
  if (!rawFrom.value || !rawTo.value) {
    return;
  }
  return rangeUtil.describeTimeRange({ from: rawFrom.value, to: rawTo.value });
});

const requiredMsg = `Please enter a past date or "now"`;

const commonRules = [
  (value) => !!value || requiredMsg,
  (value) => !!dateMath.parse(value) || requiredMsg,
];
const fromRules = ref(
  commonRules.concat([
    () => {
      return (
        !dateMath.parse(inputFrom.value) ||
        !dateMath.parse(inputTo.value) ||
        dateMath.parse(inputFrom.value) < dateMath.parse(inputTo.value, true) ||
        `"From" can't be after "To"`
      );
    },
  ])
);
const toRules = ref(commonRules);

function loadRangeShortcut(shortcut) {
  rawFrom.value = shortcut.from;
  rawTo.value = shortcut.to;
  store.setFromTo(rawFrom.value, rawTo.value);
  menu.value = false;
}

watch(
  () => rawFrom.value + rawTo.value,
  () => {
    inputFrom.value = rawFrom.value;
    inputTo.value = rawTo.value;
    synchronizePicker();
  }
);

function applyTimeRange() {
  rawFrom.value = inputFrom.value;
  rawTo.value = inputTo.value;
  store.setFromTo(rawFrom.value, rawTo.value);
  menu.value = false;
}

function cancelPicker() {
  synchronizePicker();
  fromDialog.value = false;
  toDialog.value = false;
}

function applyPickerFromDate() {
  inputFrom.value = toISO(pickerFrom.value);
  fromDialog.value = false;
}

function applyPickerToDate() {
  inputTo.value = toISO(pickerTo.value);
  toDialog.value = false;
}

function synchronizePicker() {
  pickerFrom.value = dateMath.parse(rawFrom.value).toDate();
  pickerTo.value = dateMath.parse(rawTo.value, true).toDate();
}

function zoomOut() {
  const from = store.from.toDate();
  const to = store.to.toDate();
  const diff = to - from;
  const newFrom = toISO(new Date(from.getTime() - diff / 2));
  const newTo = toISO(new Date(to.getTime() + diff / 2));
  store.setFromTo(newFrom, newTo);
}

watch(
  () => store.rawFrom + store.rawTo,
  () => {
    rawFrom.value = store.rawFrom;
    rawTo.value = store.rawTo;
  }
);
</script>
